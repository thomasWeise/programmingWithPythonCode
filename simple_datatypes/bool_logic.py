# Conjunction (and) is True iff both operands are True.
False and False
False and True
True  and False
True  and True

# Disjunction (or) is True iff at least one operand is True.
False or False
False or True
True  or False
True  or True

# Negation (not) is True iff its operand is False.
not True
not False

# Operators can be combined and grouped with parentheses.
(True or False) and ((False or True) or (False and False))

# Operators can accept any expression of result type bool.
(5 < 4) or (6 < 9 < 8)
