# Converting values of some datatypes to strings.

str(23)          # Convert the int 23 to the string "23".

str(23.5)        # Convert the float 23.5 to the string "23.5".

str(4) + str(5)  # First to-string conversion, then concatenation.

# Multiple to-string conversions and concatenations in one line.
str(1 < 5) + " is True and " + str(1 + 5) + " = 6."
